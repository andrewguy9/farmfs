class XSym(Type):
    """Implements OSX XSym link file type detector"""

    def __init__(self):
        super(XSym, self).__init__(mime="inode/symlink", extension="xsym")

    def match(self, buf: bytes) -> bool:
        """
        Detects the MS-Dos symbolic link format from OSX.
        Format of XSym files taken from section 11.7.3 of Mac OSX Internals
        """
        X = 0x58
        S = 0x53
        Y = 0x79
        M = 0x6D
        NEWLINE = 0xA
        ZERO = 0x30
        NINE = 0x39
        return (
            len(buf) >= 10
            and buf[0] == X
            and buf[1] == S
            and buf[2] == Y
            and buf[3] == M
            and buf[4] == NEWLINE
            and buf[5] >= ZERO
            and buf[5] <= NINE
            and buf[6] >= ZERO
            and buf[6] <= NINE
            and buf[7] >= ZERO
            and buf[7] <= NINE
            and buf[8] >= ZERO
            and buf[8] <= NINE
            and buf[9] == NEWLINE
        )


