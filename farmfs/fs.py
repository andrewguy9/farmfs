WalkItem = Tuple[Path, str]

def walk(
        *roots: Path,
        skip: Optional[SkipFunction] = None
        ) -> Generator[WalkItem, None, None]:
    if skip is None:
        skip = lambda p: False
    dirs = [iter(sorted(roots))]
    while len(dirs) > 0:
        curDir = dirs[-1]
        curPath = next(curDir, None)
        if curPath is None:
            dirs.pop()
        else:
            type = curPath.ftype()
            if skip(curPath):
                continue
            yield (curPath, type)
            if type is DIR:
                children = curPath.dir_list()
                dirs.append(iter(children))

def walk_path(item: WalkItem) -> Path:
    return item[0]

def walk_type(item: WalkItem) -> str:
    return item[1]